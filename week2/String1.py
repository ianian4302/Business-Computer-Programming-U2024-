LETTERS = "ABCDEFGHIJKLMNOPQRSTUVWXYZ"

# 1. 請取出字母 A
print(LETTERS[0])  # 結果: A

# 2. 請取出字母 C
print(LETTERS[2])  # 結果: C

# 3. 請取出字母 G
print(LETTERS[6])  # 結果: G

# 4. 請取出字母 Z
print(LETTERS[-1])  # 結果: Z

# 5. 請取出字母 X
print(LETTERS[-3])  # 結果: X

# 6. 請取出字母 AC
print(LETTERS[0:3:2])  # 結果: AC

# 7. 請取出字母 ICL
print(LETTERS[8:12:2])  # 結果: ICL

# 8. 請取出字母 ZWY
print(LETTERS[-1:-8:-3])  # 結果: ZWY
